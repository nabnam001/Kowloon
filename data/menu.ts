export type Cuisine =
  | "forret"
  | "kina"
  | "thailand"
  | "vietnam"
  | "vegetar"
  | "grill"
  | "dessert";

export interface Dish {
  id: string; // menu number, e.g. "42a"
  name: string; // Danish (canonical)
  desc?: string; // Danish (canonical)
  nameEn?: string; // English name (falls back to `name`)
  descEn?: string; // English description (falls back to `desc`)
  price: number;
  cuisine: Cuisine;
  spice?: 0 | 1 | 2 | 3;
  veg?: boolean; // can be made vegetarian / is vegetarian
  vegan?: boolean; // can be made vegan
  isNew?: boolean;
  popular?: boolean; // house favourite / signature dish
  allergens?: string;
  hasImage?: boolean; // whether a real photo exists; otherwise use placeholder
}

/** Returns the localized name/desc for a dish, falling back to Danish. */
export function localizeDish(
  d: Dish,
  lang: "da" | "en"
): { name: string; desc?: string } {
  if (lang === "en") {
    return { name: d.nameEn ?? d.name, desc: d.descEn ?? d.desc };
  }
  return { name: d.name, desc: d.desc };
}

